    <script>
        // Animate counters on scroll
        function animateCounters() {
            const counters = document.querySelectorAll('.counter');
            const speed = 200;

            counters.forEach(counter => {
                const target = +counter.getAttribute('data-target');
                const count = +counter.innerText;

                if (count < target) {
                    counter.innerText = Math.ceil(count + (target / speed));
                    setTimeout(animateCounters, 10);
                } else {
                    counter.innerText = target;
                }
            });
        }

        // Animate step items on scroll
        function animateSteps() {
            const stepItems = document.querySelectorAll('.step-item');

            stepItems.forEach(item => {
                const position = item.getBoundingClientRect();

                // If item is in viewport
                if (position.top < window.innerHeight - 100) {
                    item.classList.add('visible');
                }
            });
        }

        // Show notification
        function showNotification(message, type) {
            // Remove existing notification
            const existingNotification = document.querySelector('.notification');
            if (existingNotification) {
                existingNotification.remove();
            }

            // Create notification element
            const notification = document.createElement('div');
            notification.className = `notification fixed top-6 right-6 z-50 px-6 py-4 rounded-xl shadow-2xl transform transition-all duration-500 translate-x-full`;

            // Set notification style based on type
            let bgColor, icon;
            if (type === 'success') {
                bgColor = 'linear-gradient(90deg, #3CB371, #4CAF50)';
                icon = 'check-circle';
            } else if (type === 'error') {
                bgColor = 'linear-gradient(90deg, #F44336, #E53935)';
                icon = 'exclamation-circle';
            } else {
                bgColor = 'linear-gradient(90deg, #2FA4E7, #2196F3)';
                icon = 'info-circle';
            }

            notification.style.background = bgColor;

            notification.innerHTML = `
                <div class="flex items-center">
                    <i class="fas fa-${icon} text-white text-xl mr-3"></i>
                    <span class="text-white font-semibold">${message}</span>
                </div>
            `;

            // Add to body
            document.body.appendChild(notification);

            // Animate in
            setTimeout(() => {
                notification.classList.remove('translate-x-full');
            }, 10);

            // Remove after 3 seconds
            setTimeout(() => {
                notification.classList.add('translate-x-full');
                setTimeout(() => {
                    notification.remove();
                }, 500);
            }, 3000);
        }

        // Initialize when page loads
        document.addEventListener('DOMContentLoaded', () => {
            // Start counter animations
            setTimeout(animateCounters, 500);

            // Animate steps initially
            animateSteps();

            // Animate on scroll
            window.addEventListener('scroll', animateSteps);

            // Add floating animation to commission badge
            const commissionBadge = document.querySelector('.commission-glow');
            if (commissionBadge) {
                setInterval(() => {
                    commissionBadge.classList.toggle('commission-glow');
                    setTimeout(() => {
                        commissionBadge.classList.toggle('commission-glow');
                    }, 100);
                }, 3000);
            }

            // Become a mover button
            const becomeMoverBtn = document.querySelector('button:contains("Become a Mover")');
            if (becomeMoverBtn) {
                becomeMoverBtn.addEventListener('click', function() {
                    this.innerHTML = '<i class="fas fa-spinner fa-spin mr-3"></i> Redirecting...';

                    setTimeout(() => {
                        showNotification('Redirecting to mover registration page...', 'info');

                        // Reset button after delay
                        setTimeout(() => {
                            this.innerHTML = '<i class="fas fa-truck mr-3"></i> Become a Mover';
                        }, 2000);
                    }, 1000);
                });
            }

            // Learn More button
            const learnMoreBtn = document.querySelector('button:contains("Learn More")');
            if (learnMoreBtn) {
                learnMoreBtn.addEventListener('click', function() {
                    showNotification('More information about our commission system will be displayed here.', 'info');
                });
            }
        });
    </script>
